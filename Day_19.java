package Coding;

public class Day_19 {
     public static void main(String[] args) {
        // Deklarasi variabel
        int nilai = 85;

        // Percabangan if else
        if (nilai >= 90) {
            System.out.println("Nilai anda A.");
        } else if (nilai >= 80) {
            System.out.println("Nilai anda B.");
        } else if (nilai >= 70) {
            System.out.println("Nilai anda C.");
        } else {
            System.out.println("Nilai anda D atau E.");
        }
    }
}
